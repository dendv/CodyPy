# Расчитать количество парт для учеников в трёх классах

import math
print('Введите количество учеников в первом классе:')
a = int(input())
print('Введите количество учеников во втором классе:')
b = int(input())
print('Введите количество учеников в третьем классе:')
c = int(input())
p = (math.ceil(a/2)+math.ceil(b/2)+math.ceil(c/2))
print('Требуется', p, 'парт(а).')
